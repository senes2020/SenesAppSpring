package com.senes.senesapp.model;

public class AtualizacaoUsuario {

	private String cpfAtualizacao;
	
	private String emailAtualizacao;
	
	private String emailAntigo;
	
	private String numeroAtualizacao;

	public String getCpfAtualizacao() {
		return cpfAtualizacao;
	}

	public void setCpfAtualizacao(String cpfAtualizacao) {
		this.cpfAtualizacao = cpfAtualizacao;
	}

	public String getEmailAtualizacao() {
		return emailAtualizacao;
	}

	public void setEmailAtualizacao(String emailAtualizacao) {
		this.emailAtualizacao = emailAtualizacao;
	}

	public String getNumeroAtualizacao() {
		return numeroAtualizacao;
	}

	public void setNumeroAtualizacao(String numeroAtualizacao) {
		this.numeroAtualizacao = numeroAtualizacao;
	}

	public String getEmailAntigo() {
		return emailAntigo;
	}

	public void setEmailAntigo(String emailAntigo) {
		this.emailAntigo = emailAntigo;
	}

	
	

}
